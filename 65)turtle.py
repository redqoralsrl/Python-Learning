import turtle as t
#turtle 모듈을 t라는 이름으로 들고올거야

#모듈마다 좀 달라요
#터튤모듈은 함수이름을 다 외워줘야되요
#애들 교육용으로 써요 -> 블록코딩 -> 그림그리는용도
t.shape("turtle")
t.forward(100) # 대가리가 보는 방향으로 100픽셀만큼 갈거에요
t.right(90) # 대가리방향을 오른쪽으로 90도 만큼 틉니다
t.forward(100)
t.circle(50)
input() #안 적으면 거북이 모듈이 바로 꺼져 버려요

